package model;

/**
 * Enumeración que representa los posibles estados de una actividad.
 */
public enum Estado {
    /**
     * La actividad aún no ha comenzado.
     */
    NO_INICIADA,

    /**
     * La actividad está en curso.
     */
    EN_PROGRESO,

    /**
     * La actividad ha finalizado.
     */
    COMPLETADA;
}
